#import "QCloudUploadSliceDataRequest+Custom.h"

@implementation  QCloudUploadSliceDataRequest (Custom)
- (BOOL) customBuildRequestData:(NSError *__autoreleasing *)error
{
    if (![super customBuildRequestData:error]) {
        return NO;
    }
    if (self.localPath) {
        if(![self.requestData appendPartWithFileURL:[NSURL fileURLWithPath:self.localPath] name:@"filecontent" fileName:[NSUUID UUID].UUIDString  offset:self.offset sliceLength:self.sliceLength mimeType:@"filecontent" headerParamters:@{} error:error])
        {
            return NO;
        }
    }
    return YES;
}
@end
